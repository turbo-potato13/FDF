char	*ft_strcat(char *des, const char *str)
{
	int	i;
	int k;

	k = 0;
	i = 0;
	while (des[k] != '\0')
		k++;
	while (str[i] != '\0')
	{
		des[k] = str[i];
		i++;
		k++;
	}
	des[k] = '\0';
	return (des);
}
